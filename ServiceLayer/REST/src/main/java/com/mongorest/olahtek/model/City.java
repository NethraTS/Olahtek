package com.mongorest.olahtek.model;

public class City {
	String city;
	double housevalue;

	public City() {

	}

	public City(String city, double housevalue) {
		this.city = city;
		this.housevalue = housevalue;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public double getHousevalue() {
		return housevalue;
	}

	public void setHousevalue(double housevalue) {
		this.housevalue = housevalue;
	}

}
