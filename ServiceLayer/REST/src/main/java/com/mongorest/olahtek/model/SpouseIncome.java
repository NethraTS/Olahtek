package com.mongorest.olahtek.model;

public class SpouseIncome {

	int year;
	int value;

	public SpouseIncome() {

	}

	public SpouseIncome(int year, int value) {
		this.year = year;
		this.value = value;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

}
