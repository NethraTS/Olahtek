package com.mongorest.olahtek.model;

public class IncomePlot {
	double userIncome;
	double spouseIncome;
	int year;

	public double getUserIncome() {
		return userIncome;
	}

	public void setUserIncome(double userIncome) {
		this.userIncome = userIncome;
	}

	public double getSpouseIncome() {
		return spouseIncome;
	}

	public void setSpouseIncome(double spouseIncome) {
		this.spouseIncome = spouseIncome;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}
	
}
