package com.mongorest.olahtek.model;

public class CarLoanCalculator {
	
	public String action;
    public String carprice;
    public String loanterm;
	public String interestrate;
	public String downpayment;
	public String tradeinvalue;
	public String licensefee;
	public String saletax;
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
	public String getCarprice() {
		return carprice;
	}
	public void setCarprice(String carprice) {
		this.carprice = carprice;
	}
	public String getLoanterm() {
		return loanterm;
	}
	public void setLoanterm(String loanterm) {
		this.loanterm = loanterm;
	}
	public String getInterestrate() {
		return interestrate;
	}
	public void setInterestrate(String interestrate) {
		this.interestrate = interestrate;
	}
	public String getDownpayment() {
		return downpayment;
	}
	public void setDownpayment(String downpayment) {
		this.downpayment = downpayment;
	}
	public String getTradeinvalue() {
		return tradeinvalue;
	}
	public void setTradeinvalue(String tradeinvalue) {
		this.tradeinvalue = tradeinvalue;
	}
	
	public String getLicensefee() {
		return licensefee;
	}
	public void setLicensefee(String licensefee) {
		this.licensefee = licensefee;
	}
	public String getSaletax() {
		return saletax;
	}
	public void setSaletax(String saletax) {
		this.saletax = saletax;
	}
	

}
