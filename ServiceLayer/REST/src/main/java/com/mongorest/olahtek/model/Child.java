package com.mongorest.olahtek.model;

public class Child {

	String name;
	int age;
	String flag;

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}

	public Child() {

	}

	public Child(String name, int age, String flag) {
		this.name = name;
		this.age = age;
		this.flag = flag;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
