package com.mongorest.olahtek.model;

public class AIMEIndexingFactor {
	private int year;
	private double indexingFactor;

	public AIMEIndexingFactor() {

	}

	public AIMEIndexingFactor(int year, double indexingFactor) {
		this.indexingFactor = indexingFactor;
		this.year = year;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public double getIndexingFactor() {
		return indexingFactor;
	}

	public void setIndexingFactor(double indexingFactor) {
		this.indexingFactor = indexingFactor;
	}

}
